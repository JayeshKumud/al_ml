# ---------------------------------------------------------
# SVM vs Logistic Regression
# ---------------------------------------------------------
# Feature                | Logistic Regression        | SVM
# ---------------------------------------------------------
# Model Type             | Linear classifier          | Margin-based classifier
# Decision Boundary      | Linear only                | Linear or non-linear (kernels)
# Output                 | Probabilities              | Class labels
# Best For               | Linearly separable data    | Complex or non-linear data
# Interpretability       | High                       | Low
# Outlier Sensitivity    | High                       | Low
# Training Speed         | Fast                       | Slower
# Kernel Support         | No                         | Yes (rbf, linear, poly)
# When to Use            | Simple, interpretable      | Complex boundaries
# ---------------------------------------------------------
